/**
 * Obtiene una traducción específica para un idioma
 * @param {string} lang - Código de idioma (es, en, fr, it, de, ru, uk, ar)
 * @param {string} key - Clave de traducción
 * @returns {string} Texto traducido o la clave si no existe
 */
function getTranslation(lang, key) {
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    if (translations.es && translations.es[key]) {
        return translations.es[key];
    }
    if (translations.en && translations.en[key]) {
        return translations.en[key];
    }
    console.warn(`Traducción no encontrada: ${key} para idioma ${lang}`);
    return key;
}

